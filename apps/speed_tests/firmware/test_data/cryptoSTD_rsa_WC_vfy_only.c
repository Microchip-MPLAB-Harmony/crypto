cryptoST_testAPI_t const RSA_WC_vfy_only =
{
    .name = "RSA_" DATA_PACKAGE_NAME,
    .openData = ((void*)0),
    .firstTest = firstTest,
    .nextTest = nextTest,
    .closeData = ((void*)0),
};
